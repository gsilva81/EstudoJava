package conceitos;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        int a = 97;
        int b = 100;
        

        //COMPARAÇÃO (Se isso é igual aquilo)
        System.out.println(a == b);
        //MAIOR 
        System.out.println(a > b);
        //MAIOR OU IGUAL
        System.out.println(a >= b);
        //MENOR
        System.out.println(a < b);
        //MENOR IGUAL
        System.out.println(a <= b);
        //DIFERENTE
        System.out.println(a != b);
    }

}
