package conceitos;

public class OperadoresAtribuicao {
    public static void main(String[] args) {
        int a = 5;
        int b = a;
        
        a += b; // a = a + b
        a -= b; // a = a - b
        a *= b; // a = a * b
        a /= b; // a = a / b
        a %= b; // a = a % b (par ou impar represetando por 0 e 1 respectivamente)
    }
}
